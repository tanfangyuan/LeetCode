int main()
{
    Solution s;
    vector<int> data{1,2,-2,-1};
    auto res = s.threeSum(data);
    for (auto array: res) {
        for (auto val: array) {
            std:: cout << val << " ";
        }
        std::cout << std::endl;
    }
    std::cout << std::endl;
}